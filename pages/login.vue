<script>
import { mapGetters } from 'vuex'

export default {
  name: 'LoginPage',
  layout: 'empty',
  data () {
    return {
      show: false,
      password: '',
      username: ''
    }
  },
  computed: {
    ...mapGetters(['loading'])
  },
  methods: {
    login () {
      this.$store.commit('setLoading', true)
      this.$store.dispatch(
        'login',
        { username: this.username, password: this.password }
      ).then(() => this.$router.push('/')
      ).catch(e => alert(e)
      ).finally(() => this.$store.commit('setLoading', false))
    }
  }
}
</script>

